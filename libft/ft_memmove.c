#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char			*d;
	char			*s;
	unsigned int	i;

	d = (char *)dst;
	s = (char *)src;
	i = 0;
	if (dst > src)
	{
		while (n--)
			d[n] = s[n];
	}
	else
	{
		while (i < n)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (d);
}
