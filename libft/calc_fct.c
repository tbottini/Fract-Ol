#include "libft.h"

int			sub(int a, int b)
{
	return (a - b);
}

int			add(int a, int b)
{
	return (a + b);
}

int			divi(int a, int b)
{
	return (a / b);
}

int			mul(int a, int b)
{
	return (a * b);
}
