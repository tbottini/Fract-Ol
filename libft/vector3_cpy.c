#include "libft.h"

t_vct3		*vct3_cpy(t_vct3 *vct1, t_vct3 *vct2)
{
	vct1->x = vct2->x;
	vct1->y = vct2->y;
	vct1->z = vct2->z;
	return (vct1);
}
